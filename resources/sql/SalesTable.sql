-- Drop the SalesTable if it exists
IF OBJECT_ID('dbo.SalesTable', 'U') IS NOT NULL
    DROP TABLE dbo.SalesTable;
GO

-- Create the SalesTable with the necessary columns:
-- SalesID, SaleDate, CustomerName, Amount, IngestionDate
CREATE TABLE dbo.SalesTable (
    SalesID INT PRIMARY KEY,              -- Unique identifier for each sale
    SaleDate DATE NOT NULL,               -- Date of the sale
    CustomerName VARCHAR(100) NOT NULL,   -- Name of the customer
    Amount DECIMAL(10,2) NOT NULL,          -- Sale amount
    IngestionDate DATE NOT NULL           -- Date when the data was ingested
);
GO

